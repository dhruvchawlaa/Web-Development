// Concatenation
var greeting = "Hello" + " there!";
console.log(greeting); // Hello there!

// Length
console.log(greeting.length); // 12

// Slicing and Extracting Parts of a String
var tweet = "Fear is the path to the dark side.";
var slice = tweet.slice(2, 6);
console.log(slice); // ar is

// Changing Casing
console.log(tweet.toUpperCase()); // FEAR IS THE...
console.log(tweet.toLowerCase()); // fear is the...
